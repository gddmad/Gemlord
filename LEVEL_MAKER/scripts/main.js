function interpretData() {
  let d = document.getElementById("output-area").value.split("\n");
  DATA = [];
  let count = 0;
  // get char codes
  for (let y = 0; y < 20; y++) {
    let gimmeRow = [];
    for (let x = 0; x < 80; x++) {
      gimmeRow.push({code: Number(d[count])});
      count++;
    }
    DATA.push(gimmeRow);
  }
  // color codes
  for (let y = 0; y < DATA.length; y++) {
    for (let x = 0; x < DATA[y].length; x++) {
      DATA[y][x]["color"] = Number(d[count]);
      count++;
    }
  }
  // solid bool
  for (let y = 0; y < DATA.length; y++) {
    for (let x = 0; x < DATA[y].length; x++) {
      DATA[y][x]["solid"] = Number(d[count] > 0);
      count++;
    }
  }
  renderData();
}

function outputData() {
  let out = "";
  for (let y = 0; y < DATA.length; y++) {
    for (let x = 0; x < DATA[0].length; x++) {
      let i = y * DATA[0].length + x;
      out += DATA[y][x].code + "\n";
    }
  }
  for (let y = 0; y < DATA.length; y++) {
    for (let x = 0; x < DATA[0].length; x++) {
      let i = y * DATA[0].length + x;
      out += DATA[y][x].color + "\n";
    }
  }
  for (let y = 0; y < DATA.length; y++) {
    for (let x = 0; x < DATA[0].length; x++) {
      let i = y * DATA[0].length + x;
      out += (DATA[y][x].solid ? 1 : 0) + "\n";
    }
  }
  document.getElementById("output-area").value = out;
}

function fillScreen() {
  let n = document.getElementById("fill-input").value;
  for (let y = 0; y < DATA.length; y++) {
    for (let x = 0; x < DATA[0].length; x++) {
      DATA[y][x].code = n;
    }
  }
  renderData();
}

renderData();

function renderData() {
  let parent = document.getElementById("screen-container");
  parent.innerHTML = "";
  let tb = document.createElement("table");
  for (let y = 0; y < DATA.length; y++) {
    let tr = document.createElement("tr");
    for (let x = 0; x < DATA[y].length; x++) {
      let td = document.createElement("td");
      td.style.border = "1px solid black";
      td.style.backgroundColor = "black";
      td.style.textAlign = "center";
      td.style.width = "1.25vw";
      td.style.height = "1.25vw";
      td.style.color = "#" + COLOR[DATA[y][x].color];
      td.innerHTML = DATA[y][x].code;
      td.onclick = new Function("createPopup(" + x + "," + y + ")");
      tr.appendChild(td);
    }
    tb.appendChild(tr);
  }
  parent.appendChild(tb);
}

function setCell(x, y, data) {
  DATA[y][x] = data;
}

function createPopup(x, y) {
  let div = document.createElement("div");
  div.id = "popup-container";
  div.style.backgroundColor = "#ddd";

  let h1 = document.createElement("h1");
  h1.id = "popup-title";
  h1.innerHTML = x + "," + y;

  let char = document.createElement("input");
  char.id = "popup-char-input";
  char.placeholder = "ASCII Character Code";
  char.type = "number";

  let sol = document.createElement("input");
  sol.id = "popup-sol-input";
  sol.type = "checkbox";
  sol.checked = true;
  let l1 = document.createElement("label");
  l1.innerHTML = "solid";

  let col = document.createElement("select");
  col.id = "popup-col-input";
  let l2 = document.createElement("label");
  l2.innerHTML = "color";
  for (let i = 0; i < COLOR.length; i++) {
    let opt = document.createElement("option");
    opt.value = i;
    opt.style.backgroundColor = COLOR[i];
    opt.innerHTML = "COLOR " + i;
    col.appendChild(opt);
  }

  let submit = document.createElement("button");
  submit.innerHTML = "submit";
  submit.onclick = () => {
    let pos = document.getElementById("popup-title").innerHTML.split(",");
    setCell(pos[0], pos[1], {
      code: Number(document.getElementById("popup-char-input").value),
      solid: document.getElementById("popup-sol-input").checked,
      color: Number(document.getElementById("popup-col-input").options[document.getElementById("popup-col-input").selectedIndex].value)
    });
    renderData();
    document.getElementById("popup-container-container").innerHTML = "";
  };

  let cancel = document.createElement("button");
  cancel.innerHTML = "cancel";
  cancel.onclick = () => {
    document.getElementById("popup-container-container").innerHTML = "";
  };

  div.appendChild(h1);
  div.appendChild(char);
  div.appendChild(document.createElement("br"));
  div.appendChild(sol);
  div.appendChild(l1);
  div.appendChild(document.createElement("br"));
  div.appendChild(col);
  div.appendChild(l2);
  div.appendChild(document.createElement("br"));
  div.appendChild(submit);
  div.appendChild(cancel);

  document.getElementById("popup-container-container").appendChild(div);
}

